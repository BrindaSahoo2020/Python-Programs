#Examples on Python basic math Module

import math
print(math.ceil(3.4))
print(math.floor(3.4))
print(math.copysign(3, -1))
print(math.fabs(-3.4))
print(math.factorial(6))
print(math.factorial(4.0))
print(math.trunc(3.1))
print(math.exp(3))
print(int(math.pow(3, 2)))
print((math.pow(3, 2)))
print(math.sqrt(4))

